object Test extends App {
  println("1st pack (6 elements)")
  println("")
  println("foo")
  println("$foo")
  println("${foo")
  println("$foo}")
  println("${foo}")
  println("===================")
  println()

  println("2nd pack (4 elements)")
  val a = "aaa"
  val b = "bbb"
  println(a + b)
  println(a + "b")
  println("a" + b)
  println("a" + "b")
  println("===================")
  println()

  println("3rd pack (3 elements)")
  println("a" + s"b")
  println("a" + s"$b")
  println("a" + s"${b}")
  println("===================")
  println()

  println("4th pack (3 elements)")
  println(a + s"b")
  println(a + s"$b")
  println(a + s"${b}")
  println("===================")
  println()

  println("5th pack (3 elements)")
  println(s"a" + b)
  println(s"$a" + b)
  println(s"${a}" + b)
  println("===================")
  println()

  println("6th pack (3 elements)")
  println(s"a" + "b")
  println(s"$a" + "b")
  println(s"${a}" + "b")
  println("===================")
  println()

  println("7th pack (9 elements)")
  println(s"a" + s"b")
  println(s"a" + s"$b")
  println(s"a" + s"${b}")
  println(s"$a" + s"b")
  println(s"$a" + s"$b")
  println(s"$a" + s"${b}")
  println(s"${a}" + s"b")
  println(s"${a}" + s"$b")
  println(s"${a}" + s"${b}")
  println("===================")
  println()

  println("8th pack (4 elements)")
  println(s"b".getClass)
  println(a + s"b".getClass)
  println(s"b".getClass + a)
  println(s"b".getClass.toString + s"b".getClass.toString)
  println("===================")
  println()
}